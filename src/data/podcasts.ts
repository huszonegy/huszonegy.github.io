// Segédfüggvény az URL-barát név (slug) generálásához
export const slugify = (text: string) => {
    return text
        .toString()
        .toLowerCase()
        .normalize('NFD')
        .replace(/[\u0300-\u036f]/g, '') // Ékezetek eltávolítása
        .replace(/%/g, ' szazalek')    // Speciális karakterek kezelése
        .trim()
        .replace(/\s+/g, '-')          // Szóközök -> kötőjel
        .replace(/[^\w-]+/g, '')       // Egyéb jelek törlése
        .replace(/--+/g, '-');         // Dupla kötőjelek javítása
}

// Egy konkrét epizód lekérése slug alapján
export function get_pod_by_slug(slug: string) {
    return podcasts.find(p => slugify(p.name) === slug);
}

// Frissített get_pods, ami már tartalmazza a slugokat
export function get_pods(max_count: number) {
    const enriched = podcasts.map(p => ({
        ...p,
        slug: slugify(p.name)
    }));

    const sorted = enriched.sort((n1, n2) => {
        const d1 = Date.parse(n1.date);
        const d2 = Date.parse(n2.date);
        return (d2 || 0) - (d1 || 0);
    });

    const n = sorted.length;
    if (max_count <= 0 || max_count >= n) return sorted;
    return sorted.slice(0, max_count);
}

export const parseTopics = (topic: string) => {
    // Csak a " - " (szóköz-kötőjel-szóköz) mentén vágunk
    const parts = topic.split(/\s+-\s+/);
    
    return parts.map(part => {
        // Időbélyeg keresése (pl. 05:12)
        const timeMatch = part.match(/(\d{1,2}:)?\d{1,2}:\d{2}/);
        const time = timeMatch ? timeMatch[0] : null;
        
        let label = time 
            ? part.replace(time, '').replace(/^[:\s-]+/, '').trim() 
            : part.trim();

        // NAGY KEZDŐBETŰSÍTÉS:
        // Ha van szöveg, az első karaktert nagyra váltjuk, a többit változatlanul hagyjuk.
        if (label.length > 0) {
            label = label.charAt(0).toUpperCase() + label.slice(1);
        }
        
        return { time, label };
    }).filter(item => item.label.length > 0);
};

/**
 * Visszaadja az adott slug-hoz tartozó epizód előző és következő szomszédját.
 * 
 * Az epizódok sorrendje a podcast lista oldalon látható sorrend (legújabb elöl).
 * "Előző" = régebbi (listán lentebb), "Következő" = újabb (listán fentebb).
 * 
 * Az első epizódnak nincs "előző"-je, a legújabbnak nincs "következő"-je.
 */
export function get_adjacent_pods(slug: string) {
        const index = podcasts.findIndex(p => slugify(p.name) === slug);
      
        if (index === -1) return { prev: null, next: null };
      
        const withSlug = (p: typeof podcasts[number]) => ({
          ...p,
          slug: slugify(p.name),
        });
      
        return {
          prev: index < podcasts.length - 1 ? withSlug(podcasts[index + 1]) : null,
          next: index > 0 ? withSlug(podcasts[index - 1]) : null,
        };
}